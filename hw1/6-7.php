<?php
class A {
    public function foo() {
        static $x = 0; //статическая переменная принадлежащая методу класса A
        echo ++$x; //увеличение значения на 1 и последующий вывод
    }
}
class B extends A {
}
$a1 = new A();
$b1 = new B();
$a1->foo(); // Вывод $x - 1 Принадлежащей классу A
$b1->foo(); // Вывод $x - 1 Принадлежащей классу B, наследуемой от A
$a1->foo(); // 2
$b1->foo(); // 2 От класса B
//Вывод 1122